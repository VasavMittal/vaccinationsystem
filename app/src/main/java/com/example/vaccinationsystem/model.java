package com.example.vaccinationsystem;

public class model {
    String ap1,ap2,ap3;

    public model() {
    }

    public model(String ap1, String ap2, String ap3) {
        this.ap1 = ap1;
        this.ap2 = ap2;
        this.ap3 = ap3;
    }

    public String getAp1() {
        return ap1;
    }

    public void setAp1(String ap1) {
        this.ap1 = ap1;
    }

    public String getAp2() {
        return ap2;
    }

    public void setAp2(String ap2) {
        this.ap2 = ap2;
    }

    public String getAp3() {
        return ap3;
    }

    public void setAp3(String ap3) {
        this.ap3 = ap3;
    }
}
